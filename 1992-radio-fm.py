ytdl_format_options = {
    'format': 'bestaudio/best',
    'outtmpl': '%(extractor)s-%(id)s-%(title)s.%(ext)s',
    'restrictfilenames': True,
    'noplaylist': True,
    'nocheckcertificate': True,
    'ignoreerrors': False,
    'logtostderr': False,
    'quiet': True,
    'no_warnings': True,
    'default_search': 'auto',
    'source_address': '0.0.0.0' # bind to ipv4 since ipv6 addresses cause issues sometimes
}

ffmpeg_options = {
    'options': '-vn'
}

ytdl = youtube_dl.YoutubeDL(ytdl_format_options)


class YTDLSource(discord.PCMVolumeTransformer):
    def __init__(self, source, *, data, volume=0.5):
        super().__init__(source, volume)

        self.data = data

        self.title = data.get('title')
        self.url = data.get('url')

    @classmethod
    async def from_url(cls, url, *, loop=None, stream=False):
        loop = loop or asyncio.get_event_loop()
        data = await loop.run_in_executor(None, lambda: ytdl.extract_info(url, download=not stream))

        if 'entries' in data:
            # take first item from a playlist
            data = data['entries'][0]

        filename = data['url'] if stream else ytdl.prepare_filename(data)
        return cls(discord.FFmpegPCMAudio(filename, **ffmpeg_options), data=data)


class Music(commands.Cog):
    def __init__(self, bot):
        self.bot = bot
        self.volume = 1   

    @commands.command()
    async def play(self, ctx, *, query):
        """Plays a file from the local filesystem"""

        source = discord.PCMVolumeTransformer(discord.FFmpegPCMAudio(query))
        ctx.voice_client.play(source, after=lambda e: print(f'Player error: {e}') if e else None)

        await ctx.send(f'Now playing: {query}')

    @commands.command()
    async def yt(self, ctx, *, url):
        """Plays from a url (almost anything youtube_dl supports)"""

        async with ctx.typing():
            player = await YTDLSource.from_url(url, loop=self.bot.loop)
            ctx.voice_client.play(player, after=lambda e: print(f'Player error: {e}') if e else None)

        await ctx.send(f'Now playing: {player.title}')

    @commands.command()
    async def stream(self, ctx, *, url):
        """Streams from a url (same as yt, but doesn't predownload)"""

        async with ctx.typing():
            player = await YTDLSource.from_url(url, loop=self.bot.loop, stream=True)
            ctx.voice_client.play(player, after=lambda e: print(f'Player error: {e}') if e else None)

        await ctx.send(f'Now playing: {player.title}')

    @commands.command()
    async def volume(self, ctx, volume: int):
        """Changes the player's volume"""       
        if ctx.voice_client is None:
            return await ctx.send("Not connected to a voice channel.")
        if volume < 1 or volume > 100:
            return await ctx.send("Please choose a number between 1 and 100.") 
        volume = volume / 100     
        reaction = 'ðŸ”Š' if volume > self.volume else 'ðŸ”‰'
        self.volume = volume
        ctx.voice_client.source.volume = self.volume
        await ctx.message.add_reaction(reaction)

    @commands.command()
    async def mute(self, ctx):
        """sets volume of the bot to 0 and unmutes otherwise"""
        if ctx.voice_client.source.volume == 0:
            ctx.voice_client.source.volume = self.volume
            await ctx.message.add_reaction('ðŸ”Š')
        else:
            ctx.voice_client.source.volume = 0
            await ctx.message.add_reaction('ðŸ”‡')

    @commands.command()
    async def unmute(self, ctx):
        """Sets volume back to normal"""        
        ctx.voice_client.source.volume = self.volume
        await ctx.message.add_reaction('ðŸ”Š')
    @commands.command()
    async def on(self, ctx):
        """Joins the voice channel and play a radio"""
        if ctx.author.voice is None:
            return await ctx.channel.send("Please connect to a Voice Channel to use this command.")
        channel = ctx.author.voice.channel
        if ctx.voice_client is not None:
            await ctx.voice_client.move_to(channel)
        else:
            await channel.connect()
        #TODO: PLAY MUSIC
    
    @commands.command()
    async def off(self, ctx):
        """Stops and disconnects the bot from voice"""

        await ctx.voice_client.disconnect() 
      
    @play.before_invoke
    @yt.before_invoke
    @stream.before_invoke
    async def ensure_voice(self, ctx):
        if ctx.voice_client is None:
            if ctx.author.voice:
                await ctx.author.voice.channel.connect()
            else:
                await ctx.send("You are not connected to a voice channel.")
                raise commands.CommandError("Author not connected to a voice channel.")
        elif ctx.voice_client.is_playing():
            ctx.voice_client.stop()

bot = commands.Bot(command_prefix=commands.when_mentioned_or("tune."),
                   description='Relatively simple music bot example')

